// Icon shown on an interaction marker: a speech glyph while the conversation
// is still ongoing (outcome is null), then a state-specific glyph once it ends.
//
// With outcomeIcons off this is the original three-way mapping, unchanged, so
// the island looks exactly as it does today. With it on the marker reads from
// the shared table in lib/outcomes.ts -- which is what finally gives truce,
// tension, and amicable their own glyphs and stops the island, the transcript
// panel, and the activity feed from disagreeing about what a truce looks like.
function convNodeGlyph(outcome: ClientConversation["outcome"]): string {
  if (islandFlags.outcomeIcons) return outcomePresentation(outcome).icon;
  if (outcome === "alliance") return "🤝"; // handshake
  if (outcome === "fight") return "⚔"; // crossed swords
  return "💬"; // ongoing conversation
}

// Normalize a spoken line for the bubble: collapse the whitespace (a stray
// newline would otherwise force a break the wrap never asked for) and hard-cap
// the length before wrapping. Returns null for a blank or missing line so the
// bubble degrades to the listening dots rather than rendering an empty box.
function bubbleLine(raw: string | undefined): string | null {
  const text = (raw ?? "").replace(/\s+/g, " ").trim();
  if (!text) return null;
  return text.length > BUBBLE_MAX_CHARS ? text.slice(0, BUBBLE_MAX_CHARS) : text;
}

