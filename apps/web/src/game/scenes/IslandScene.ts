// Allowances added around the framed box so the things that hang off a sprite
// are never clipped. Authored in SCREEN pixels because that is what they
// actually are: the tag pill and the speech bubble both render counter-scaled
// by 1/zoom (see update()), so their on-screen size is fixed and their WORLD
// size GROWS as the camera zooms out. followPadWorld() therefore converts them
// at the live zoom. Baking the world size in at one rung -- which is what this
// did first -- is correct only at that rung and under-reserves headroom at
// every wider one, and a wider rung is exactly the case a spread-out pair
// forces, so that is precisely when a three-line bubble got clipped off the
// top of the viewport.
//
// Sideways: half a speech bubble, BUBBLE_WRAP_W (132) + BUBBLE_PAD_X * 2 (10),
// halved = 71.
//
// Upward, measured from the sprite's FEET (its origin) through the tallest
// stack that can ever sit above it: the pill's half height above its center
// (~10) + BUBBLE_GAP (3) + BUBBLE_TAIL_H (4) + a three-line bubble body
// (BUBBLE_MAX_LINES at ~13px line height + BUBBLE_PAD_Y * 2 = ~47) = ~64.
