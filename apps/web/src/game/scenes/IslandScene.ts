// lower in frame, under-reserving clips the top line of a bubble, and only one
// of those is a bug (same reasoning as CONV_PANEL_INSET above). The head
// offset itself (CHAR_ORIGIN_Y * CHAR_FRAME_H + PILL_GAP) is NOT counter-
// scaled -- update() applies it in world units -- so it is added separately,
// unconverted, in followPadWorld.
const FOLLOW_PAD_SIDE_SCREEN = 71;
const FOLLOW_PAD_HEAD_SCREEN = 76;
// Floor for the zoom those screen allowances are converted at. Without it the
